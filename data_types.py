# String (str) adalah tipe data yang digunakan untuk menyimpan teks. String dapat terdiri dari satu atau lebih karakter, termasuk huruf, angka, simbol, dan spasi. String biasanya didefinisikan dengan menggunakan tanda kutip tunggal (' ') atau tanda kutip ganda (" ").
print("Hello, World!") # Menulis tulisan , contoh string menggunakan tanda kutip ganda
print('Halo teman-teman AI Development Infinite Learning Batch 10 - Angkatan 3') # Menulis tulisan , contoh string menggunakan tanda kutip tunggal

# Integer (int) adalah tipe data yang digunakan untuk menyimpan bilangan bulat, baik positif maupun negatif. Integer tidak memiliki bagian desimal dan dapat digunakan untuk melakukan operasi matematika seperti penjumlahan, pengurangan, perkalian, dan pembagian.
print(19000000) # Menulis angka , contoh integer
print(-19000000) # Menulis angka , contoh integer

# Float (float) adalah tipe data yang digunakan untuk menyimpan bilangan desimal atau bilangan pecahan. Float dapat digunakan untuk merepresentasikan nilai yang lebih presisi daripada integer, terutama ketika bekerja dengan angka yang memiliki bagian desimal.
print(3.14) # Menulis angka , contoh float
print(-3.14) # Menulis angka , contoh float

# Boolean (bool) adalah tipe data yang digunakan untuk menyimpan nilai benar atau salah. Boolean hanya memiliki dua nilai, yaitu True (benar) dan False (salah). Tipe data ini sering digunakan dalam logika pemrograman untuk membuat keputusan berdasarkan kondisi tertentu.
print(True) # Menulis nilai boolean , contoh True
print(False) # Menulis nilai boolean , contoh False

print(int(89.9)) # Mengkonversi float menjadi integer, hasilnya adalah 3 karena bagian desimal dihilangkan
print(float(9123))
print(bool(0))